/**
 * This defines a class that listens for events coming from an IRCConnector.
 */

package com.packethammer.vaquero.net;

public abstract class IRCConnectorListener {
    /**
     * This occurs when it's okay to begin sending data to the remote IRC 
     * server. It is implied that it is before the login process.
     */
    public void onConnected() {
        
    }
    
    /**
     * This occurs when the session with the remote IRC server has finished.
     * This can be of our own doing, or of the remote server's doing.
     */
    public void onConnectionClosed() {
        
    }
}
