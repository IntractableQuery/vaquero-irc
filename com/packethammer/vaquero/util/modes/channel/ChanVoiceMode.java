/*
 * Represents channel voice status.
 *
 * Derived from rfc1459.
 */

package com.packethammer.vaquero.util.modes.channel;

public class ChanVoiceMode extends ChannelNicknameParameterMode {
    /**
     * Initializes this channel voice mode with a nickname to operate on
     * and if we are giving or removing their voice status in the channel.
     *
     * @param nick The user's nickname.
     * @param adding Determines if we are giving or removing this mode.
     */
    public ChanVoiceMode(String nick, boolean adding) {
        super(MODE_VOICE, nick, adding);
    }
}
