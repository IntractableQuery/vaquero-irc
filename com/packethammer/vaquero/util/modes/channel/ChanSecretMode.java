/*
 * This mode makes a channel secret. 
 *
 * Derived from rfc1459.
 */

package com.packethammer.vaquero.util.modes.channel;

public class ChanSecretMode extends ChannelMode {
    /**
     * Initializes this secret mode to determine if it is being
     * given or removed from a channel.
     *
     * @param beingAdded True if this mode should be given, or false if it should be removed.
     */
    public ChanSecretMode(boolean beingAdded) {
        super(MODE_SECRET, beingAdded);
    }
}
