/*
 * This mode prevents users who are not opped or voiced from talking in the channel.
 *
 * Derived from rfc1459.
 */

package com.packethammer.vaquero.util.modes.channel;

public class ChanModeratedMode extends ChannelMode {    
    /**
     * Initializes this moderated mode to determine if it is being
     * given or removed from a channel.
     *
     * @param beingAdded True if this mode should be given, or false if it should be removed.
     */
    public ChanModeratedMode(boolean beingAdded) {
        super(MODE_MODERATED, beingAdded);
    } 
}
