/*
 * This mode prevents users not in the channel from messaging it. 
 *
 * Derived from rfc1459.
 */

package com.packethammer.vaquero.util.modes.channel;

public class ChanNoExtMsgMode extends ChannelMode {
    /**
     * Initializes this no-external-messages mode to determine if it is being
     * given or removed from a channel.
     *
     * @param beingAdded True if this mode should be given, or false if it should be removed.
     */
    public ChanNoExtMsgMode(boolean beingAdded) {
        super(MODE_NOEXTERNALMESSAGES, beingAdded);
    }
}
